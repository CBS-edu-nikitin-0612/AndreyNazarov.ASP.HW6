namespace Task2
{
    public class CalcService
    {
        public int Div(int left, int right)
        {
            return left / right;
        }
        public int Add(int left, int right)
        {
            return left + right;
        }
        public int Mul(int left, int right)
        {
            return left * right;
        }
        public int Sub(int left, int right)
        {
            return left - right;
        }
    }
}
